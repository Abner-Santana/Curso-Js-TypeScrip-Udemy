/**Diferença entre var / let / const*/

/**
 * 
 * var (escopo global) - escopo fora do bloco.
 * let (escopo local) - escopo restrito ao bloco.
 * const (não permitindo reatribuição e nem redeclaração) - constante.

OBS 1: Ao declarar uma variável sem o var, sem o let ou sem o const, ela é criada no escopo global.

OBS 2: Nunca declare variáveis sem var, let e const e evite a utilização de var.
 */